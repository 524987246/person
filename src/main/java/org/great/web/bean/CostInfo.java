package org.great.web.bean;

import java.io.Serializable;

import org.springframework.context.annotation.Scope;
import org.springframework.stereotype.Component;

/**
 * 消费信息Bean
 * 
 * @author xiej
 * @date 2016-12-27
 * @since 1.0
 */
@Component
@Scope("prototype")
public class CostInfo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 3677515173978423941L;
	/**
	 * 主键id
	 */
	private Integer sid;
	/**
	 * 支付名称
	 */
	private String payname;
	/**
	 * 支付金额
	 */
	private String paymoney;
	/**
	 * 创建时间
	 */
	private String createtime;
	/**
	 * 结束时间
	 */
	private String endtime;
	/**
	 * 备注
	 */
	private String remark;
	/**
	 * 收入或者支出
	 */
	private Integer income_expend;
	/**
	 * 使用者
	 */
	private String user;
	/**
	 * 状态:1启用2禁用
	 */
	private Integer isemploy;

	public Integer getSid() {
		return sid;
	}

	public void setSid(Integer sid) {
		this.sid = sid;
	}

	public String getPayname() {
		return payname;
	}

	public void setPayname(String payname) {
		this.payname = payname;
	}

	public String getPaymoney() {
		return paymoney;
	}

	public void setPaymoney(String paymoney) {
		this.paymoney = paymoney;
	}

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}

	public String getEndtime() {
		return endtime;
	}

	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Integer getIncome_expend() {
		return income_expend;
	}

	public void setIncome_expend(Integer income_expend) {
		this.income_expend = income_expend;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public Integer getIsemploy() {
		return isemploy;
	}

	public void setIsemploy(Integer isemploy) {
		this.isemploy = isemploy;
	}

	@Override
	public String toString() {
		return "CostInfo [sid=" + sid + ", payname=" + payname + ", paymoney="
				+ paymoney + ", createtime=" + createtime + ", endtime="
				+ endtime + ", remark=" + remark + ", income_expend="
				+ income_expend + ", user=" + user + ", isemploy=" + isemploy
				+ "]";
	}

}
